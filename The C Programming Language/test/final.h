
/* Function that returns the first position of  
   the integer 'b', if exists in the array. If 
   does not exist, then it returns -1. */

int exists(int *arr, int arr_size, int b);

/* Function that returns 0 or 1 depending on 
   whether the integer 'b' appears only once  
   or has at least one duplicate. */ 

int duplicate(int *arr, int arr_size, int b);

/* Function that returns number of appearences 
   of the integer 'b' in the given array. If not 
   present at all, then the function returns 0. */

int frequency(int *arr, int arr_size, int b);
 
